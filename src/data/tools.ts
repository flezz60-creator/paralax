export function getToolBySlug(slug: string): ToolDefinition | undefined {
  return tools.find((tool) => tool.slug === slug);
}

export function getLiveTools(): ToolDefinition[] {
  return tools.filter((tool) => tool.status === "live");
}

export function getToolsByCluster(cluster: ToolClusterId): ToolDefinition[] {
  return tools.filter((tool) => tool.cluster === cluster);
}

export function getRelatedTools(tool: ToolDefinition): ToolDefinition[] {
  const relatedSet = new Set(tool.related);
  return tools.filter((candidate) => candidate.slug !== tool.slug && relatedSet.has(candidate.slug));
}

export function getPlannedTools(): ToolDefinition[] {
  return tools.filter((tool) => tool.status === "planned");
}
